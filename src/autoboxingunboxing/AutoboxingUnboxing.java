package autoboxingunboxing;

public class AutoboxingUnboxing {
    public static void main(String[] args) {
        // autoboxing envovemos variables de tipo primitvo en objetos
        Integer entero=10;
        System.out.println(entero.intValue());
        int enteroprimi = entero;
        System.out.println(enteroprimi);
    }
}
